def seed():
    import urllib.request
    import json

    print(f"Seeding {len(SAMPLES)} transactions to {API_URL}...")

    for i, tx in enumerate(SAMPLES):
        data = json.dumps(tx).encode()
        req = urllib.request.Request(
            f"{API_URL}/transactions",
            data=data,
            headers={"Content-Type": "application/json"},
        )
        try:
            with urllib.request.urlopen(req, timeout=10) as resp:
                result = json.loads(resp.read())
                print(f"  [{i+1}] id={result['id'][:8]}... "
                      f"fraud_prob={result['fraud_probability']:.4f} "
                      f"is_fraud={result['is_fraud']} ({result['confidence']})")
        except Exception as e:
            print(f"  [{i+1}] FAILED: {e}")

    print("Done!")


if __name__ == "__main__":
    seed()
